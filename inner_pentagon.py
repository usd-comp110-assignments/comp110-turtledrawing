"""
Module: inner_pentagon

Program to draw two pentagons using turtles, one inside of the other (non-overlapping).
"""

import turtle

# create a turtle and set the pen color
sat = turtle.Turtle()
sat.pencolor("red")

# draw the pentagon
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)
sat.forward(50)
sat.left(72)


